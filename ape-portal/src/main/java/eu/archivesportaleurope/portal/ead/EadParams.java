package eu.archivesportaleurope.portal.ead;

public class EadParams {
	private String eadDisplayId;
	private Integer xmlTypeId;
	private String eadid;
	private Integer aiId;
	private String repoCode;
	private String xmlTypeName;
	private Integer pageNumber;


	public String getEadDisplayId() {
		return eadDisplayId;
	}
	public void setEadDisplayId(String eadDisplayId) {
		this.eadDisplayId = eadDisplayId;
	}
	public Integer getXmlTypeId() {
		return xmlTypeId;
	}
	public void setXmlTypeId(Integer xmlTypeId) {
		this.xmlTypeId = xmlTypeId;
	}
	public String getEadid() {
		return eadid;
	}
	public void setEadid(String eadid) {
		this.eadid = eadid;
	}
	public Integer getAiId() {
		return aiId;
	}
	public void setAiId(Integer aiId) {
		this.aiId = aiId;
	}
	public String getRepoCode() {
		return repoCode;
	}
	public void setRepoCode(String repoCode) {
		this.repoCode = repoCode;
	}
	public String getXmlTypeName() {
		return xmlTypeName;
	}
	public void setXmlTypeName(String xmlTypeName) {
		this.xmlTypeName = xmlTypeName;
	}
	public Integer getPageNumber() {
		return pageNumber;
	}
	public void setPageNumber(Integer pageNumber) {
		this.pageNumber = pageNumber;
	}

}
